let s;

// slash
s = "south\\north";
console.log(s);
s = 'south\\north';
console.log(s);

// quote
s = "south'north";
console.log(s);
s = "south\"north";
console.log(s);
s = 'south"north';
console.log(s);
s = 'south\'north';
console.log(s);

// newline
s = "south\nnorth";
console.log(s);
s = 'south\nnorth';
console.log(s);
